namespace D_DCharLists
{
  /// <summary>
  /// Действия с экипировкой.
  /// </summary>
  public enum EnumWorkWithEquipment
  {
    /// <summary>
    /// Оборудования было изменено.
    /// </summary>
    EquipmentWasChange,

    /// <summary>
    /// Выбрать броню.
    /// </summary>
    ChooseArmor,

    /// <summary>
    /// Выбрать что экипировано в руке.
    /// </summary>
    ChooseWhatEquipInHand,

    /// <summary>
    /// Экипировка доспехи.
    /// </summary>
    ArmorEquip,

    /// <summary>
    /// Экипировка в правой руке. 
    /// </summary>
    RightHandEquip,

    /// <summary>
    /// Экипировка в левой руке.
    /// </summary>
    LeftHandEquip,

    /// <summary>
    /// Доспех отсутствует.
    /// </summary>
    ArmorOff,

    /// <summary>
    /// Правая рука отключена.
    /// </summary>
    RightHandOff,

    /// <summary>
    /// Левая рука отключена.
    /// </summary>
    LeftHandOff,

    /// <summary>
    /// Нет доспеха в инвентаре.
    /// </summary>
    NoArmorInInventory
  }
}